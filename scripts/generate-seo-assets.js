const fs = require("fs");
const path = require("path");

const siteConfig = require("../lib/seo/siteConfig.json");

function normalizeSiteUrl(value) {
  const trimmed = String(value || "").trim();

  if (!trimmed) {
    return siteConfig.defaultSiteUrl;
  }

  return trimmed.replace(/\/+$/, "");
}

function normalizePathname(value) {
  const trimmed = String(value || "/").trim();

  if (!trimmed || trimmed === "/") {
    return "/";
  }

  return trimmed.startsWith("/") ? trimmed : `/${trimmed}`;
}

function buildAbsoluteUrl(siteUrl, pathname) {
  const normalizedPath = normalizePathname(pathname);

  return normalizedPath === "/"
    ? `${siteUrl}/`
    : `${siteUrl}${normalizedPath}`;
}

function escapeXml(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&apos;");
}

const siteUrl = normalizeSiteUrl(
  process.env.EXPO_PUBLIC_SITE_URL || siteConfig.defaultSiteUrl
);
const publicDir = path.join(__dirname, "..", "public");
const generatedDate = new Date().toISOString().slice(0, 10);

const sitemapXml = [
  '<?xml version="1.0" encoding="UTF-8"?>',
  '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">',
  ...siteConfig.publicRoutes.map((route) => {
    const location = buildAbsoluteUrl(siteUrl, route.path);

    return [
      "  <url>",
      `    <loc>${escapeXml(location)}</loc>`,
      `    <lastmod>${generatedDate}</lastmod>`,
      `    <changefreq>${escapeXml(route.changeFrequency)}</changefreq>`,
      `    <priority>${Number(route.priority).toFixed(1)}</priority>`,
      "  </url>",
    ].join("\n");
  }),
  "</urlset>",
  "",
].join("\n");

const robotsTxt = [
  "# Generated by scripts/generate-seo-assets.js",
  "User-agent: *",
  "Allow: /",
  ...siteConfig.disallowPaths.map((routePath) => `Disallow: ${routePath}`),
  "",
  `Sitemap: ${siteUrl}/sitemap.xml`,
  "",
].join("\n");

fs.mkdirSync(publicDir, { recursive: true });
fs.writeFileSync(path.join(publicDir, "sitemap.xml"), sitemapXml, "utf8");
fs.writeFileSync(path.join(publicDir, "robots.txt"), robotsTxt, "utf8");

console.log(`Generated sitemap.xml and robots.txt for ${siteUrl}`);
